package FinalExam;

public class Card {
	
	private String cardCompany;
	private String ccNum;
	private String securityCode;
	private String expDate;
	
	public String getCardCompany() {
		return cardCompany;
	}
	
	public void setCardCompany(String cc) {
		cardCompany = cc;
	}
	
	public String getCCNum() {
		return ccNum;
	}
	
	public void setCCNum(String num) {
		ccNum = num;
	}
	
	public String getSecurityCode() {
		return securityCode;
	}
	
	public void setSecurityCode(String sc) {
		securityCode = sc;
	}
	
	public String getExpDate() {
		return expDate;
	}
	
	public void setExpDate(String ed) {
		expDate = ed;
	}
}
